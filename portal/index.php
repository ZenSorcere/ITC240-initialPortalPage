    <h2 class="subheader">About Mike!</h2>
       
       <p>Welcome to my ITC 240 Portal Website! This site is for my php class as part of my second quarter in the Web Development Certificate program at Seattle Central College.</p>

       <p>My background started in Game Quality Assurance Testing. I dipped my toe into those waters through Volt/VMC, where I did game certification testing for the original Xbox before it was released. However, this was right after the Dotcom Fallout of 2000, so I found myself working along-side people who were new as well as those who had been doing testing for several years. This didnt bode well for my future in the field. When my contract ended, I moved to other career paths, until a few years later where I was hired at a company called Screenlife, LLC (most famously known for the "Scene It?" Trivia board games). I did DVD boardgame testing for them for 7 years before the company was liquidated.</p>

       <p>Just before Screenlife ended, I applied for and was hired at Nordstrom as a Test Lead for their Interactive Development team.  During the 5 years I was at Nordstrom, I built the test team up from one tester and myself doing content verification, to an 8 person team doing feature development testing as part of a DevOps team, creating new features for the Nordstrom.com website. By the time a re-org disolved our team, I had become the Test Program Manager. </p>
       
       <p>After some time unemployed and a couple years juggling cell tower construction drawings for a telecom company as a Project Manager, I decided it was time to go back to school and pursue coding skills, which landed me at Seattle Central's WebDev Cert program. </p>

       <h3>Extracurriculars</h3>

       <p>When not pursuing day job or higher education elements, I am heavily involved in Seattle's local theater community. It pretty much takes up all my free time, between performing in them and regularly seeing over 100 performances a year. In addition to acting in several shows over the last decade, I've also found an artistic home at <a href="http://www.annextheatre.org/" target="_blank" title="Annex Theatre">Annex Theatre</a>, where I've been on the volunteer staff as the Bar Manager since 2010.</p>


       <p>I also perform as a regular cast member in <i>The Panel Jumper</i>, a webseries about comic book history and lore. I play the oft-beleaguered comic book store manager, Manager Mike.  You can check out Panel Jumper episodes at <a href="http://thepaneljumper.com/home/" target="_blank" title="The Panel Jumper webseries"><strong>www.thepaneljumper.com</strong></a>.</p>
